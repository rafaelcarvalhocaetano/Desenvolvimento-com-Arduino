    public void read() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) lista.getModel();
        model.setNumRows(0);
        Crud dao = new Crud();

        for (Dados d : dao.listar()) {
            model.addRow(new Object[]{
                d.getId(),
                d.getNome()
            });
        }

    }

